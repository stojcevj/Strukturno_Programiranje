int elka(int n){
	int i,j;
	for(i=0;i<n/2+1;i++){
		for(j=0;j<n;j++){
			if(j<=n/2+i&&j>=n/2-i){
				printf("*");
			}
            else{
				printf(" ");
			}
		}
		printf("\n");	
	}
}
int main(){
	int n;
	scanf("%d",&n);
	elka(n);
}

